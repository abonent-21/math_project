# Модуль NZER_N_B
# Проверка на ноль: если число не равно нулю, то «да» иначе «нет»

# Автор: Самигулин Д.А. Группа - ПМИ-3381

def NZER_N_B(A):
    """
    Проверяет, является ли натуральное число A ненулевым.
    Возвращает True, если A не равно нулю, иначе False.

    :param A: натуральное число (список цифр)
    :return: True, если A != 0, иначе False
    """
    # Проходим по всем цифрам числа A
    for digit in A:
        if digit != 0:
            return True  # Если есть ненулевая цифра, число не равно нулю
    return False  # Все цифры нули, число равно нулю
